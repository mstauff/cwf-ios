import UIKit
extension UIColor {
    
    public class var CWFNavBarTintColor: UIColor{
        return UIColor(red: 0.07, green: 0.494, blue: 0.652, alpha: 1.0)
    }
    
    public class var CWFDarkGreenColor: UIColor{
        return UIColor(red: (22.0/255), green: (130.0/255), blue: (30.0/255), alpha: 1.0)
    }
    
    public class var CWFGreyTextColor: UIColor {
        return UIColor(red: (125.0/255), green: (125.0/255), blue: (125.0/255), alpha: 1.0)
    }

    public class var CWFGreenTextColor: UIColor {
        return UIColor(red: (64.0/255), green: (225.0/255), blue: (63.0/255), alpha: 1.0)
    }
    
    
}
